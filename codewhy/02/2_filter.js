nums = [12, 234, 566, 77, 432];
// filter:过滤
// 回调(item(,index,nums)=>{}
nums.filter((item, index, arr) => {
  console.log("1", item);
  console.log("2", index);
  console.log("3", arr);
  if (item % 2 == 0) {
    console.log("偶数是：", item);
  }
});

